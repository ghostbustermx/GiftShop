    public partial class PinnacleSoftwareContext : DbContext
    {
        public PinnacleSoftwareContext()
        {
        }

        public PinnacleSoftwareContext(DbContextOptions<PinnacleSoftwareContext> options)
            : base(options)
        {
        }

        public virtual DbSet<AspNetRoleClaims> AspNetRoleClaims { get; set; }
        public virtual DbSet<AspNetRoles> AspNetRoles { get; set; }
